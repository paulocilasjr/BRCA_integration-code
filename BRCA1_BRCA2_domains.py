def get_features_df(gene: str) -> pd.DataFrame:
    """
    Return features for 'BRCA1' or 'BRCA2' as a DataFrame with columns:
        feature, start_aa, end_aa
    """
    g = gene.strip().upper()
    if g == "BRCA1":
        return pd.DataFrame(BRCA1_FEATURES)
    if g == "BRCA2":
        return pd.DataFrame(BRCA2_FEATURES)
    raise ValueError("gene must be 'BRCA1' or 'BRCA2'")
