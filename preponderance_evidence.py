import pandas as pd

# Helper to pick strongest sub-classification
def get_strongest(labels, precedence):
    for cls in precedence:
        if cls in labels:
            return cls
    return None

# Main classification function
def classify_variants(nested_lists):
    results = []
    for lst in nested_lists:
        # strip off the "(T###)" suffix
        labels = [item.split('(')[0] for item in lst]

        # count broad categories
        counts = {
            'PS': sum(1 for L in labels if L.startswith('PS3')),
            'BS': sum(1 for L in labels if L.startswith('BS3')),
            'Hypomorph': sum(1 for L in labels if L.lower().startswith('hypomorph')),
            'Indeterminate': sum(1 for L in labels if L.startswith('Indeterminate')),
        }

        non_zero = [cat for cat, ct in counts.items() if ct > 0]
        # concordant if only one category present, otherwise discordant
        status = 'concordant' if len(non_zero) == 1 else 'discordant'

        # Case: only a single category present
        if len(non_zero) == 1:
            cat = non_zero[0]
            if cat == 'PS':
                strongest = get_strongest(labels, ['PS3', 'PS3_moderate', 'PS3_supporting'])
                body = f"-, {strongest}, Pathogenic"
            elif cat == 'BS':
                strongest = get_strongest(labels, ['BS3', 'BS3_moderate', 'BS3_supporting'])
                body = f"-, {strongest}, Benign"
            elif cat == 'Hypomorph':
                body = "-, VUS, Hypomorph"
            else:  # Indeterminate only
                body = "-, VUS, Indeterminate"
        else:
            # find top category and sum of others
            top_cat, top_ct = max(counts.items(), key=lambda kv: kv[1])
            sum_others = sum(counts.values()) - top_ct

            # check 3:1 ratio
            if top_cat == 'PS' and sum_others > 0 and top_ct >= 3 * sum_others:
                strongest = get_strongest(labels, ['PS3', 'PS3_moderate', 'PS3_supporting'])
                body = f"pathogenic, {strongest}, Pathogenic"
            elif top_cat == 'BS' and sum_others > 0 and top_ct >= 3 * sum_others:
                strongest = get_strongest(labels, ['BS3', 'BS3_moderate', 'BS3_supporting'])
                body = f"benign, {strongest}, Benign"
            else:
                body = "Indeterminate, VUS, Indeterminate"

        # combine status with the rest of the classification
        results.append(f"{status}, {body}")

    return results


